# 2.hacer un programa que calcule e imprima el salario semanal de un empleado a partir de 
# sus horas semanales trabajadas y de su salario por hora

salario_hora = int(input(' ingrese cuanto le pagan por hora: '))
horas_trabajadas = int(input(' ingrese cuantas horas trabajo en la semana: '))

